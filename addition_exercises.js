////ONE WAY TO ADD VARIABLES
var blueCars = 4;
var redCars = 5;
var totalCars = blueCars + redCars;
console.log("I have " + totalCars + " cars!");

////THE HANDY WAY TO DO VARIABLES
var tapasCount = 4;
tapasCount++;
console.log("I have " + tapasCount + " tapas!");

////A SHORT HAND WAY TO ADD AN ARBITRARY NUMBER TO THE VALUE OF A VARIABLE
var booksCount = 10;
booksCount += 20; //adds 20 books
console.log("I have read " + booksCount + " books!");